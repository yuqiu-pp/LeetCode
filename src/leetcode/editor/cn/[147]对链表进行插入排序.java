/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
class Solution {
    public ListNode insertionSortList(ListNode head) {
        if (head == null) {
            return head;
        }
        // å“¨å…µç»“ç‚¹
        ListNode first = new ListNode(0);
        first.next = head;
        ListNode curr = head.next;
        ListNode last = head;
        while(curr != null) {
            if (last.val <= curr.val) {
                last = last.next;
            } else {
                // å•é“¾è¡¨ï¼Œä»å¤´å¼€å§‹æ‰¾æ’å…¥ä½ç½®
                ListNode prev = first;
                while (prev.next.val <= curr.val) {
                    prev = prev.next;
                }
                // æ’å…¥åœ¨preçš„å‰é¢
                last.next = curr.next;
                curr.next = prev.next;
                prev.next = curr;
            }
            curr = last.next;
        }
        return first.next;
    }
}
